package siva06.jse.equality;

public class EqualityOperators {

	public static void main(String[] args) {
		int x = 10;
		int y = 20;

		if (x == y) {
			System.out.println("x is equality to y");
		} else if (x != y) {
			System.out.println("x is not equal to y");
		} else {
			System.out.println("sorry");
		}

	}
}
