package siva02.jse.arithematic;

public class ArithematicOperators {

	public static void main(String[] args) {

		int a = 10;
		int b = 7;
		System.out.println(a + b); // (10+7)addition
		System.out.println(a - b); // (10-7) subtraction
		System.out.println(a * b); // (10 * 7) multiplication
		System.out.println(a / b); // (10/7)division
		System.out.println(a % b); // (10 %7)remainder

		System.out.println(10 * 10 / 5 + 3 - 1 * 4 / 2); // expression

	}

}
